// การจัดรูปแบบ Strings
// มาโคร format! ช่วยให้เราสร้างสตริงด้วยการ กำหนดรูปแบบด้วยพารามิเตอร์ โดยยึดตำแหน่งที่จะนำค่ามาแทนที่เอาไว้(เข่น {})

// format! ใช้วิธีการกำหนดพารามิเตอร์แบบเดียวกับ println!

// ความสามารถของมันค่อนข้างกว้างเกินกว่าขอบเขตที่ Tour of Rust จะอธิบายได้ทั้งหมด แต่สามารถไปดูเพิ่มเติมได้ ที่นี่ https://doc.rust-lang.org/std/fmt/.

fn main() {
    let a = 42;
    let f = format!("secret to life: {}",a);
    println!("{}",f);
}

